void sortBubbleDesc(int array[]) {
	int count = 0;
	for (int i = sizeArray - 1; i >= 0; i--)
	{
		for (int j = sizeArray - 1; j >= 0; j--)
		{
			if (array[j] < array[j + 1]) {
				int temp = array[j];
				array[j] = array[j + 1];
				array[j + 1] = temp;
			}
			count++;
		}
	}
	cout << "Array was sorted in " << count << " steps" << endl;
}

